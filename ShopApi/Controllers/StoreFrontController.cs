                return Ok(_storeBL.GetOrderbyStoreID(storeID));
            }
            catch (System.Exception ex)
            {
                return StatusCode(422, ex.Message);
            }
        }

        // GET: api/Store/
        [HttpGet("ViewInventory/{storeID}")]
        public IActionResult GetProductbyStoreID(int storeID)
        {
            try
